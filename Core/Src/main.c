void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
    //NOTE: All pins are different Pin but also different Ports
    switch (GPIO_Pin) {
        case BUT1_Pin:
            event_enqueue(&hevent_queue, BUT1);
            //printf("BUT1 pressed\r\n");
            break;
        case BUT2_Pin:
            event_enqueue(&hevent_queue, BUT2);
            //printf("BUT2 pressed\r\n");
            break;
        case BUT3_Pin:
            event_enqueue(&hevent_queue, BUT3);
            //printf("BUT3 pressed\r\n");
            break;
        case BUT4_Pin:
            event_enqueue(&hevent_queue, BUT4);
            //printf("BUT4 pressed\r\n");
            break;
        case BUT5_Pin:
            //printf("BUT5 pressed\r\n");
            encoder_callback(&hencoder, BUT5_Pin);
            break;
        case ENC_A_Pin:
            encoder_callback(&hencoder, ENC_A_Pin);
            break;
        case ENC_B_Pin:
            encoder_callback(&hencoder, ENC_B_Pin);
            break;
        case SW_C_Pin:
#ifndef DISABLE_HEATER_DOOR_DETECTION
            uint32_t cur_time = HAL_GetTick();
            // Rising edge detected
            if (HAL_GPIO_ReadPin(SW_C_GPIO_Port, SW_C_Pin) == GPIO_PIN_SET) {
                //debounce
                if (!sw_c_flag && cur_time >= sw_c_last_interrupt_time + SW_C_DEBOUNCE_TIME)
                {
                    sw_c_last_interrupt_time = cur_time;
                    sw_c_flag = 1;
                    heater.flag_door_open = 0;
                    /* SSR windowing checks flag_door_open every update (1s) */
                }
            }
            // Falling edge detected
            else
            {
                //debounce
                if (sw_c_flag && cur_time >= sw_c_last_interrupt_time+SW_C_DEBOUNCE_TIME)
                {
                    sw_c_last_interrupt_time = cur_time;
                    sw_c_flag = 0;
                    heater.flag_door_open = 1;
                    /* SSR windowing checks flag_door_open every update (1s) */
                }
            }
#endif
            break;
        default:
            break;
    }
}
